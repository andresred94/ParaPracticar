package EjerciciosColecciones.Listas.Ejercicio2;

public class Color {
    private String color;
    private String ansi;

    public Color(String color, String ansi) {
        this.color = color;
        this.ansi = ansi;
    }
    public  Color(){}

    public String getColor() {
        return color;
    }

    public String getAnsi() {
        return ansi;
    }
}
